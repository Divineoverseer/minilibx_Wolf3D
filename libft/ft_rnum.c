#include "libft.h"

int		ft_rnum(const char *s, char c)
{
	int			i;

	i = 0;
	while (*s)
	{
		while (*s == c && *s)
			s++;
		while (*s != c && *s)
			s++;
		if (*s != '\0')
			i++;
	}
	return (i);
}
