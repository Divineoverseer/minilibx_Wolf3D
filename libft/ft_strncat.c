#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*temp;

	temp = s1 + ft_strlen(s1);
	while (n-- && *s2)
		*temp++ = *s2++;
	*temp = '\0';
	return (s1);
}
