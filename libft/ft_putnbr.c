#include "libft.h"

void	ft_putnbr(int n)
{
	long int	nn;

	nn = n;
	if (nn < 0)
	{
		nn = -nn;
		ft_putchar('-');
	}
	if (nn > 9)
		ft_putnbr(nn / 10);
	ft_putchar((nn % 10) + '0');
}
